    private void abrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abrirActionPerformed
        // TODO add your handling code here:
        int returnVal = jFileChooser1.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser1.getSelectedFile();
            try {
                DefaultTableModel dadosFile;
                try ( //Lendo o arquivo
                        BufferedReader line = new BufferedReader(new FileReader(file.getAbsolutePath()))) {
                    String linha= line.readLine();
                    String[] linhas= linha.split(";");
                    Object [][] valores = null;
                    //Adicionando cabeçalho a tabela
                    table.setModel(new DefaultTableModel(valores,linhas));
                    colunasComboBox.removeAllItems();
                    //Adicionando cabeçalho a combobox
                    for (String linha1 : linhas) {
                        colunasComboBox.addItem(linha1);
                        AVLTree rootAno1= new AVLTree();
                        trees.add(rootAno1);
                    }   //Adicionando valores a suas respectivas árvores
                    dadosFile = (DefaultTableModel)table.getModel();
                    linha = line.readLine();
                    while(linha!= null){
                        linhas = linha.split(";");
                        //dadosFile.addRow(linhas);
                        Dado dados= new Dado(linhas);
                        for(int i=0; i<linhas.length; i++){
                            trees.get(i).insert(linhas[i], dados);
                        }
                        linha = line.readLine();
                    }}
                //Adicionando dados adicionados na tabela
                trees.get(0).printAllTree(dadosFile);
                palavraTextField.requestFocus();
              // What to do with the file, e.g. display it in a TextArea
             // textarea.read( new FileReader( file.getAbsolutePath() ), null );
            } catch (IOException ex) {
              System.out.println("problem accessing file"+file.getAbsolutePath());
            }
        } else {
            System.out.println("File access cancelled by user.");
        }
    }//GEN-LAST:event_abrirActionPerformed

