    private void verTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verTodosActionPerformed
        // TODO add your handling code here:
        if(!trees.isEmpty()){
            DefaultTableModel dadosFile= (DefaultTableModel)table.getModel();
            limparTabela(dadosFile);
            trees.get(0).printAllTree(dadosFile);
        }else
            JOptionPane.showMessageDialog(verTodos, "Nenhum arquivo foi carregado! Carrega um aí, é de graça.","Floresta EDII", 0);
    }//GEN-LAST:event_verTodosActionPerformed
    public void limparTabela(DefaultTableModel modelo){
        int row = modelo.getRowCount();
        for(int i=0; i<row; i++)
            modelo.removeRow(0);
            modelo.fireTableRowsDeleted(0, row);
    }
